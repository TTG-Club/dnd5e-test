/**
 * Приводит произвольное название к машинному ключу (slug): нижний регистр,
 * только латиница и цифры, всё остальное сворачивается в дефисы, а ведущие и
 * замыкающие дефисы срезаются. Например, «Bounty Hunter» → «bounty-hunter».
 *
 * Используется при генерации ключей видов и предысторий, создаваемых в мире.
 *
 * @param value - исходное название
 * @returns slug-строка (может быть пустой, если латиницы и цифр в названии нет)
 */
export function slugify(value: string): string {
  return value
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}
