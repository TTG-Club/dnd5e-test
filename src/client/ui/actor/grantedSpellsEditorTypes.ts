/**
 * Контракт переиспользуемого редактора выдаваемых заклинаний
 * ({@link GrantedSpellsEditor}). Может применяться не только у видов, но и у
 * черт/классов/предметов — везде, где нужно «вписать заклинание + связать с
 * компендиумом + выбрать пак».
 */

/**
 * Заклинание компендиума ИЗ КОНКРЕТНОГО ПАКА (лёгкая проекция Spell). Одно и то
 * же заклинание может встретиться несколькими вариантами — по одному на пак.
 */
export interface SpellOption {
  id: string;
  name: string;
  nameEn?: string;
  /** Ключ источника-книги (для подписи «PHB» и т.п.). */
  sourceKey?: string;
  /** id пака-компендиума (манифеста), откуда вариант. */
  packId: string;
  /** Отображаемое имя пака (напр. «SRD Бэкап»). */
  packName: string;
}
