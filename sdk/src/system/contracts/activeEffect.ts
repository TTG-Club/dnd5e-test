/**
 * Нейтральный контракт активного эффекта.
 *
 * Кросс-катные (не D&D-специфичные) формы активного эффекта, которые ядро/сцена
 * знают напрямую: провенанс (`EffectOrigin`), длительность (`EffectDuration`),
 * аура (`EffectAura`), триггер области (`AreaEffectTrigger`) и сама нейтральная
 * база эффекта `BaseActiveEffect`. Конкретная система (D&D 5e) наследует базу и
 * уточняет числовые модификаторы/флаги/состояние своими типами
 * (см. `system/dnd/activeEffectTypes.ts` → `ActiveEffect extends BaseActiveEffect`).
 *
 * ЖИВЁТ В КОНТРАКТЕ (а не в system/dnd), чтобы корневой barrel `@vtt/shared` и
 * нейтральные типы (`BaseActor`/`BaseCreature`/`BaseGameItem`/`CustomArea`) могли
 * ссылаться на форму эффекта БЕЗ зависимости от движка D&D (готовность к Ф3 —
 * физическому переезду system/dnd в systems/dnd5e).
 *
 * @module system/contracts/activeEffect
 */

import type { EffectOrigin } from './effects.js';

// ── Длительность ──────────────────────────────────────────────

/** Тип длительности эффекта */
export type EffectDurationType =
  | 'permanent'
  | 'rounds'
  | 'minutes'
  | 'hours'
  | 'days'
  | 'turn'
  | 'special';

/**
 * Якорь хода для точной длительности `type: 'turn'`: на чьём ходу истекает.
 * - `carrier` — ход самого носителя эффекта (цели);
 * - `source` — ход источника эффекта (кастера; нужен `ActiveEffect.sourceActorId`).
 */
export type EffectTurnAnchor = 'carrier' | 'source';

/** Момент хода якоря, в который истекает точная длительность `type: 'turn'` */
export type EffectTurnTiming = 'start' | 'end';

/**
 * Длительность эффекта.
 *
 * - `permanent` — не истекает
 * - `rounds` — истекает через N раундов (декремент через Initiative Tracker)
 * - `minutes/hours/days` — информационное, без автодекремента
 * - `special` — произвольное описание длительности
 */
export interface EffectDuration {
  /** Тип длительности */
  type: EffectDurationType;
  /** Начальное количество единиц (для rounds/minutes/hours/days) */
  value?: number;
  /** Оставшееся количество (для rounds, декрементируется автоматически) */
  remaining?: number;
  /**
   * Якорь хода для `type: 'turn'` — чей ход прекращает эффект (носитель/источник).
   * По умолчанию `carrier`.
   */
  turnAnchor?: EffectTurnAnchor;
  /** Момент хода якоря для `type: 'turn'` (начало/конец). По умолчанию `end`. */
  turnTiming?: EffectTurnTiming;
  /**
   * Для `type: 'turn'`: пропустить ПЕРВУЮ подходящую границу хода якоря.
   * Проставляется при наложении (см. `stampTurnDuration`): если эффект наложен
   * во время хода якоря, конец этого хода — «текущий», а не «следующий» ход,
   * поэтому первую границу пропускаем. Декрементируется сервером при смене хода.
   */
  turnSkipFirst?: boolean;
}

// ── Аура ──────────────────────────────────────────────────────

/**
 * Настройки Ауры для эффекта.
 */
export interface EffectAura {
  /** Радиус в футах */
  radius: number;
  /** Кого задевает аура */
  target: 'allies' | 'enemies' | 'all';
  /** Применяется ли эффект к создателю ауры */
  applyToSelf: boolean;
  /** Отображать ли радиус ауры на сцене */
  visible?: boolean;
}

// ── Триггер области ───────────────────────────────────────────

/**
 * Триггер срабатывания эффекта области/ауры:
 * - `stay` (по умолчанию) — эффект висит на сущности, пока она внутри области,
 *   и снимается при выходе (текущее поведение);
 * - `enter` — разовое срабатывание в момент входа (урон/статус), сам носитель в
 *   `activeEffects` не остаётся (если только нагрузка не содержит длящийся статус);
 * - `exit` — разовое срабатывание в момент выхода.
 *
 * Срабатывание `enter`/`exit` происходит на каждый вход/выход (повторный заход
 * в область проигрывает эффект заново).
 */
export type AreaEffectTrigger = 'stay' | 'enter' | 'exit';

// ── Нейтральная база эффекта ──────────────────────────────────

/**
 * Нейтральная база активного эффекта — кросс-катные VTT-поля, которые ядро/сцена
 * читают напрямую (id/имя/иконка/провенанс/длительность/аура/триггер области), а
 * также обобщённые модификаторы. Числовые модификаторы (`changes`) и флаги
 * (`flags`) здесь непрозрачны (`unknown[]`/`string[]`) — ядро лишь копирует их
 * (аура-пропагация) и сопоставляет `conditionKey` для бейджей; их точную форму
 * задаёт система (D&D: `EffectChange[]`/`EffectFlagKey[]`/`ConditionKey`).
 *
 * Зеркало `BaseActor`/`BaseCreature`/`BaseGameItem`. D&D-наследник добавляет
 * боевые поля (спасброски/урон/периодику/иммунитеты) и сужает `changes`/`flags`/
 * `conditionKey` до своих типов.
 */
export interface BaseActiveEffect {
  /** Уникальный идентификатор эффекта */
  id: string;
  /** Название эффекта */
  name: string;
  /** Описание эффекта */
  description: string;
  /** Путь к иконке (формат tabler:icon-name) */
  icon?: string;
  /** Отключён ли эффект (временно деактивирован, но не удалён) */
  disabled: boolean;
  /** Источник эффекта */
  origin: EffectOrigin;
  /** ID объекта-источника (предмета, заклинания и т.д.) */
  originId?: string;
  /**
   * ID сущности-источника (кастера/атакующего), наложившей эффект. Нужен для
   * точной длительности `type: 'turn'` с якорем `source` и для провенанса.
   */
  sourceActorId?: string;
  /** Переносится ли эффект с предмета на актора при экипировке */
  transfer: boolean;
  /** Длительность эффекта */
  duration: EffectDuration;
  /** Числовые модификаторы — форму задаёт система (ядро копирует опаково) */
  changes: unknown[];
  /** Булевые флаги-теги — форму задаёт система (ядро копирует опаково) */
  flags: string[];
  /** Настройки ауры (если эффект транслируется на других) */
  aura?: EffectAura;
  /**
   * Триггер для эффектов области/ауры. Если не задан — `stay` (эффект висит,
   * пока сущность внутри). `enter`/`exit` — разовое срабатывание нагрузки.
   */
  areaTrigger?: AreaEffectTrigger;
  /**
   * Цель применения эффекта.
   * - `'self'` (по умолчанию) — применяется к владельцу при экипировке
   * - `'target'` — применяется к цели при попадании атакой
   */
  effectTarget?: 'self' | 'target';
  /**
   * Ключ состояния, если эффект представляет стандартное состояние. Ядро
   * сопоставляет его для бейджей/иммунитетов; точную форму (`ConditionKey`)
   * задаёт система.
   */
  conditionKey?: string;
}
