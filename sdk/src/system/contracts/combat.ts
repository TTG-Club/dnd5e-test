/**
 * Нейтральные контрактные типы боевого взаимодействия (урон/атака).
 *
 * Кросс-катные формы, которыми оперирует контракт `VttSystem` и нейтральное ядро
 * (`targetStore`): результат применения урона и контекст входящей атаки. Конкретная
 * система (D&D и др.) конформит и реэкспортит их для своих потребителей.
 *
 * @module system/contracts/combat
 */

/**
 * Контекст входящей атаки по защитнику — для условных бонусов к КД (напр. «Щит
 * ловли стрел»: +2 КД от дальнобойных). Нейтральный дискриминатор типа атаки.
 */
export interface IncomingAttackContext {
  /** Тип входящей атаки: рукопашная, дальнобойная или заклинание */
  attackType: 'melee' | 'ranged' | 'spell';
}

/**
 * Категория сработавшей защиты от урона (для меток/сводок в чате). Кросс-катный
 * концепт «как ответили защиты цели»; конкретные значения система задаёт по-своему.
 */
export type DamageDefenseOutcome =
  | 'immunity'
  | 'resistance'
  | 'vulnerability'
  | 'normal';

/**
 * Результат применения урона/лечения к сущности (для формирования меток).
 * Возвращается контрактным методом `VttSystem.applyDamageToEntity`.
 */
export interface DamageApplyResult {
  /** Имя сущности-цели */
  actorName: string;
  /** ХП до применения */
  hpBefore: number;
  /** ХП после применения */
  hpAfter: number;
  /** Урон, поглощённый временными ХП */
  tempAbsorbed?: number;
  /** Сработавшая защита от урона (для пометки в метке) */
  defenseOutcome?: DamageDefenseOutcome;
}
