/**
 * Источник активного эффекта — откуда он пришёл. Кросс-катный VTT-концепт
 * (провенанс), одинаковый для любой системы; используется ядром для меток и
 * фильтрации эффектов, а системой — при выдаче/откате.
 */
export type EffectOrigin =
  | 'item'
  | 'spell'
  | 'feature'
  | 'condition'
  | 'manual'
  | 'area';
