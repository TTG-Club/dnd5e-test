/**
 * Типы манифестов для модулей и систем VTT.
 * Разделено на клиентскую (ClientModuleManifest), серверную (ModuleManifest)
 * и систему (SystemManifest) с общей базой.
 *
 * @module types/module
 */

/**
 * Допустимые разрешения для клиентских модулей.
 *
 * Ровно эти 5 секций API закрыты разрешением (см. `createClientModuleAPI`).
 * Остальные секции (`hooks`/`extensions`/`modules`/`scene`/`chat`/`dice`/`audio`/
 * `journal`/`system`) доступны всегда и разрешения не требуют — поэтому в этом
 * типе их нет (раньше были `chat`/`dice`/`audio`/`scene`, но проверок по ним не
 * было — инертные значения удалены).
 */
export type ModulePermission =
  | 'settings'
  | 'socket'
  | 'notifications'
  | 'keybindings'
  | 'modals';

/**
 * Базовый манифест с общими полями для всех (клиент, сервер, система).
 */
export interface BaseManifest {
  /** Уникальный ID (kebab-case) */
  id: string;
  /** Человекочитаемое название */
  name: string;
  /** Семантическая версия */
  version: string;
  /** Описание (опционально) */
  description?: string;
  /** Автор (опционально) */
  author?: string;
}

/**
 * Манифест клиентского модуля для динамической загрузки (в браузере).
 */
export interface ClientModuleManifest extends BaseManifest {
  /** Иконка модуля (tabler/ttg коллекция, напр. 'tabler:puzzle') */
  icon?: string;
  /**
   * Клиентская конфигурация (поведение: JS-точка входа + стили).
   * Опционально: модуль может быть чисто контентным (только `compendium/`).
   */
  client?: {
    /** Точка входа JS (относительно папки модуля) */
    entry: string;
    /** CSS-файлы (относительно папки модуля) */
    styles?: string[];
  };
  /** Запрошенные разрешения модуля */
  permissions?: ModulePermission[];
  /** Модуль добавляет компендиум (есть папка `compendium/` с манифестом) */
  hasCompendium?: boolean;
  /**
   * ID совместимых игровых систем: `['*']` или отсутствует — совместим со всеми;
   * иначе список должен содержать активную систему мира. Симметрично
   * `ModuleManifest.compatibleSystems`; используется для раздачи клиентской части
   * модуля только в совместимых с системой мира.
   */
  compatibleSystems?: string[];
}

/**
 * Манифест серверного модуля VTT (module.json).
 * Определяет метаданные модуля, его зависимости и совместимость с игровыми системами.
 */
export interface ModuleManifest extends BaseManifest {
  /** Системный модуль (встроенный в сервер) */
  isSystem?: boolean;
  /** Названия миров, в которых установлен модуль */
  installedInWorlds?: string[];
  /** ID модулей, которые должны быть загружены до этого */
  dependencies?: string[];
  /**
   * ID совместимых систем.
   * Массив строк или `['*']` для совместимости со всеми.
   * По умолчанию — все системы.
   */
  compatibleSystems?: string[];
}

/**
 * Происхождение системы, ВЫЧИСЛЯЕМОЕ СЕРВЕРОМ по корню, из которого прочитан
 * манифест: `builtin` — встроенная (предсобранная, доверенная) папка приложения;
 * `user` — пользовательская папка `<installData>/systems` (недоверенная).
 */
export type SystemOrigin = 'builtin' | 'user';

/**
 * Манифест игровой системы (system.json).
 */
export interface SystemManifest extends BaseManifest {
  /** Единица измерения расстояний по умолчанию */
  defaultDistanceUnit?: 'ft' | 'm';
  /**
   * Путь к директории справочных данных (JSON-файлы).
   * Относительно корня директории системы.
   */
  dataPath?: string;
  /**
   * Клиентская часть системы (UI) для РАНТАЙМ-загрузки в браузере — по аналогии с
   * `ClientModuleManifest.client`. Пользовательская (`origin='user'`) система,
   * несущая собственный интерфейс (листы/карточки/модалки), объявляет здесь свою
   * JS-точку входа и стили; клиент грузит их как `<script>`/`<link>` из
   * `/system-assets/<id>/…` (раздаётся сервером ТОЛЬКО для ДОВЕРЕННОЙ системы) и
   * даёт коду самозарегистрироваться через `globalThis.VTTSystems`. У встроенных
   * систем не используется — их UI вкомпилирован в бандл приложения.
   */
  client?: {
    /** Точка входа JS (относительно папки системы) */
    entry: string;
    /** CSS-файлы (относительно папки системы) */
    styles?: string[];
  };
  /**
   * ID встроенного (доверенного) движка правил, который использует ДЕКЛАРАТИВНАЯ
   * пользовательская система (напр. `'dnd5e'` для варианта «D&D 2014 на движке
   * 5e»). Задаётся АВТОРОМ user-системы в `system.json`. Сервер НЕ исполняет код
   * user-системы — правила берутся из объявленного доверенного движка. У
   * встроенных систем не используется (они несут собственный исполняемый код).
   */
  engine?: string;
  /**
   * Происхождение — ВЫЧИСЛЯЕТСЯ СЕРВЕРОМ по корню манифеста, НЕ читается из JSON
   * (значение в файле игнорируется/перезаписывается). Определяет модель доверия.
   */
  origin?: SystemOrigin;
  /**
   * Доверенность — ВЫЧИСЛЯЕТСЯ СЕРВЕРОМ по корню (`builtin` → true, `user` →
   * false). Инвариант безопасности: сервер НИКОГДА не выполняет `import()` кода
   * недоверенной (user) системы; такая система обязана быть декларативной и
   * объявлять доверенный `engine`. Значение в JSON игнорируется.
   */
  trusted?: boolean;
  /**
   * Система ПОСТАВЛЯЕТСЯ приложением (есть её образец в `dist/system-templates`) —
   * ВЫЧИСЛЯЕТСЯ СЕРВЕРОМ, не читается из JSON.
   *
   * Она живёт в той же папке, что и чужие (`origin='user'`), но это НАШ код: он
   * авто-доверяется при высеве, поэтому отзывать у неё доверие незачем, а удалённую
   * можно вернуть из образца («Восстановить»). Для чужих систем `shipped=false`:
   * им нужен ручной гейт доверия и восстанавливать их неоткуда.
   */
  shipped?: boolean;
  /**
   * ПОСТАВЛЯЕМАЯ система УДАЛЕНА (её папки нет) — ВЫЧИСЛЯЕТСЯ СЕРВЕРОМ по РЕАЛЬНОСТИ
   * (есть образец, но нет папки), а НЕ по флагу в стейте высева: тот ставится только
   * при старте, поэтому свежее удаление «Пересканировать» иначе не увидел бы.
   *
   * Такая система остаётся ВИДНА в списке (`GET /api/systems`), чтобы её можно было
   * вернуть кнопкой «Восстановить» (`POST /api/systems/:id/restore`), но ПОЛЬЗОВАТЬСЯ
   * ею нельзя: `collectSystemEntries` её не отдаёт (миры на ней не запускаются),
   * ассеты не раздаются, выбрать её при создании мира нельзя. Удалили — значит
   * удалили: из приложения она НЕ подхватывается.
   */
  removed?: boolean;
}
