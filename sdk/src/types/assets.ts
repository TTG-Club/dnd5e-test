/**
 * Типы файлового менеджера мира (AssetBrowser).
 *
 * Контракт между сервером (`/api/assets`, `/api/upload`) и клиентом:
 * сервер формирует записи в этом формате, клиент валидирует и отображает.
 */

/** Тип записи файлового менеджера мира */
export type WorldAssetType = 'image' | 'video' | 'audio' | 'folder';

/** Запись файлового менеджера мира — файл или папка в директории мира */
export interface WorldAsset {
  /** Имя файла или папки (без пути) */
  name: string;
  /** Размер файла в байтах (для папок 0) */
  size: number;
  /** Дата последнего изменения в формате ISO 8601 */
  modified: string;
  /** Тип записи */
  type: WorldAssetType;
  /** URL для загрузки файла (например, `/world/maps/cave.png`) */
  url: string;
  /** Путь относительно корня папки мира (например, `maps/cave.png`) */
  relativePath: string;
  /**
   * URL статического постера видео (первый кадр в WebP), если он сгенерирован
   * рядом с файлом. Только для `type === 'video'` — даёт дешёвое превью без
   * загрузки тяжёлого видео.
   */
  posterUrl?: string;
}
